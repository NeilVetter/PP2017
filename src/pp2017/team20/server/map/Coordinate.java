package pp2017.team20.server.map;

public class Coordinate {
/** 
 * Klasse um die Koordinaten von x und y zu bestimmen
 * 
 * @author Hamid, Kirli , 6041663 **/
	public int xcoord;
	public int ycoord;
	
	public Coordinate(int xcoord, int ycoord){
		this.xcoord=xcoord;
		this.ycoord=ycoord;
	}
	
	public void setCoordinate(int xcoord, int ycoord){
		this.xcoord=xcoord;
		this.ycoord=ycoord;
	}
	
}
